    $checkUrl = $apiUrl . "getChatMember?chat_id=$channelUsername&user_id=$userId";
    $result = json_decode(file_get_contents($checkUrl), true);
    if ($result['result']['status'] === 'left' || $result['result']['status'] === 'kicked') {
        return false;
    }
    return true;
}

